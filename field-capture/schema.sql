CREATE POLICY "Service role full access" ON fc_invite_codes FOR ALL USING (true) WITH CHECK (true);
CREATE POLICY "Service role full access" ON fc_building_members FOR ALL USING (true) WITH CHECK (true);
CREATE POLICY "Service role full access" ON fc_units FOR ALL USING (true) WITH CHECK (true);
CREATE POLICY "Service role full access" ON fc_equipment_types FOR ALL USING (true) WITH CHECK (true);
CREATE POLICY "Service role full access" ON fc_walk_sessions FOR ALL USING (true) WITH CHECK (true);
CREATE POLICY "Service role full access" ON fc_unit_visits FOR ALL USING (true) WITH CHECK (true);
CREATE POLICY "Service role full access" ON fc_captures FOR ALL USING (true) WITH CHECK (true);

