ALTER TABLE fc_equipment_types ENABLE ROW LEVEL SECURITY;
ALTER TABLE fc_walk_sessions ENABLE ROW LEVEL SECURITY;
ALTER TABLE fc_unit_visits ENABLE ROW LEVEL SECURITY;
